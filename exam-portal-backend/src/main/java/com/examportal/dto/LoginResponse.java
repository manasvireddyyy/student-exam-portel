package com.examportal.dto;



public class LoginResponse {

  private String token;

  // Constructor that sets the token

  public LoginResponse(String token) {

    this.token = token;

  }

  // Getter for token

  public String getToken() {

    return token;

  }

  // Setter for token

  public void setToken(String token) {

    this.token = token;

  }

}









